    public void setPergunta(int idPergunta){
        String titulo;
        String[] alternativas = new String[8];
        
        titulo = meioAmbienteController.getTituloPergunta(idPergunta);
        alternativaCorreta = meioAmbienteController.getAlternativasCorreta(idPergunta);
        alternativas = meioAmbienteController.getAlternativasPergunta(idPergunta);
        
        txtQuestao.setText(titulo);
        
        labelScore.setText("Score: " + score);
        
        btnAlternativaA.setText(alternativas[0]);
        btnAlternativaB.setText(alternativas[1]);
        btnAlternativaC.setText(alternativas[2]);
        btnAlternativaD.setText(alternativas[3]);
    }
    
