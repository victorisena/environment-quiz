    public void validarResposta(int alternativaEscolhida){
        if(meioAmbienteController.validaAlternativa(numeroQuestao, alternativaEscolhida))
        {
            
            score += 10;
            
            if(score != 50)
            {
                numeroQuestao += 1;

                this.setPergunta(numeroQuestao);
            }
            else
            {
                labelScore.setText("Score: " + score);
                
                JOptionPane.showMessageDialog(null, "Parabéns, você venceu !!!!");
                JOptionPane.showMessageDialog(null, "Seu score foi: " + score + ", você receberá em forma de dinheiro a sua compensação.");
                JOptionPane.showMessageDialog(null, "Obrigado, por participar você receberá em alguns dias o valor em sua conta."
                        + "\n Dados da conta :"
                        + "\nNome do Beneficiado: " + usuarioLogado[1] + " " + usuarioLogado[2]
                        + "\nRg: " + usuarioLogado[5]
                        + "\nCpf: " + usuarioLogado[4]
                        + "\nTelefone: " + usuarioLogado[9]
                        + "\nBanco: " + usuarioLogado[8]
                        + "\nAgência: " + usuarioLogado[6]
                        + "\nConta: " + usuarioLogado[7]
                        + "\nValor depositado: R$ " + score + ",00");
                
                this.dispose();
                
                menu.setVisible(true);
            }
            
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Alternativa Incorreta, você perdeu !");
            
            JOptionPane.showMessageDialog(null, "Seu score foi: " + score + ", agradecemos pela participação no jogo, até a Próxima.");
            
            this.dispose();
            
            menu.setVisible(true);
        }
    }

