    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed

        String login = txtLogin.getText();
        
        String senha = txtSenha.getText();
        
        if(login != null && senha != null){
            boolean retornoLogin = usuarioController.loginUsuario(login, senha);
        
            if(retornoLogin == true)
            {
                String[] usuarioLogado = new String[10];
                
                usuarioLogado = usuarioController.getDadosUsuarioByLogin(login, senha);
                
                this.dispose();

                Menu menu = new Menu();
                
                menu.setUsuarioController(usuarioController);
                
                menu.setUsuarioLogado(usuarioLogado);

                menu.setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Login/Senha inv√°lidos.");
            }
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

